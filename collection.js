function getLanguage() {
  return localStorage.getItem("portfolio-language") === "en" ? "en" : "de";
}

function getCollectionText(property) {
  const language = getLanguage();
  return i18n[language]?.collections?.[key]?.[property] || collection[property];
}

function getGroupText(group, property) {
  const language = getLanguage();
  return i18n[language]?.groups?.[group.title]?.[property] || group[property];
}

function getLinkLabel(linkKey, fallback) {
  const language = getLanguage();
  return i18n[language]?.links?.[linkKey] || fallback;
}

function setupLanguageToggle() {
  const header = document.querySelector(".site-header");

  if (!header || header.querySelector(".language-toggle")) {
    return;
  }

  const toggle = document.createElement("div");
  toggle.className = "language-toggle";
  toggle.setAttribute("aria-label", "Sprache wechseln");
  toggle.innerHTML = `
    <button type="button" data-lang="de">DE</button>
    <span>/</span>
    <button type="button" data-lang="en">EN</button>
  `;
  header.append(toggle);

  const sync = () => {
    const language = getLanguage();
    document.documentElement.lang = language;
    toggle.querySelectorAll("button").forEach((button) => {
      button.classList.toggle("active", button.dataset.lang === language);
    });
  };

  toggle.querySelectorAll("button").forEach((button) => {
    button.addEventListener("click", () => {
      localStorage.setItem("portfolio-language", button.dataset.lang);
      window.location.reload();
    });
  });

  sync();
}

const root = document.querySelector("#collection-root");
const key = document.body.dataset.collection;
const collection = collections[key];
const activeIndex = Math.max(collectionLinks.findIndex(([, , linkKey]) => linkKey === key), 0);

function fileName(path) {
  return path.split("/").pop().replace(/\.[^.]+$/, "");
}

function slug(value) {
  return value.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");
}

function formatNoteText(value) {
  return value.replace(/\n{2,}/g, "<br><br>");
}

function renderItem(item, index, options = {}) {
  const isVideo = /\.(mp4|webm|mov)$/i.test(item);

  if (isVideo) {
    const posterTime = Number.isFinite(options.posterTime) ? options.posterTime : 0.1;

    return `
      <article class="collection-item collection-video-item">
        <button class="collection-image-button collection-video-button" type="button" data-full="${item}" data-type="video">
          <video src="${item}#t=${posterTime}" muted playsinline preload="metadata"></video>
          <span>Film ansehen</span>
        </button>
      </article>
    `;
  }

  return `
    <article class="collection-item">
      <button class="collection-image-button" type="button" data-full="${item}" data-type="image">
        <img src="${item}" alt="${collection.title} ${index + 1}">
      </button>
    </article>
  `;
}

function renderGallery(items, label) {
  return `
    <section class="collection-gallery" aria-label="${label}">
      ${items.map(renderItem).join("")}
    </section>
  `;
}

function renderGroup(group, index) {
  const heading = group.href
    ? `<a href="${group.href}" target="_blank" rel="noreferrer">${group.title}</a>`
    : group.title;
  const headingExtra = key === "portraits" && group.title === "Einzelportraits"
    ? `<a class="collection-heading-jump" href="#${slug("Gruppen")}">/ Gruppen</a>`
    : "";
  const noteText = getGroupText(group, "note");
  const note = noteText ? `<p>${formatNoteText(noteText)}</p>` : "";
  const actionText = getGroupText(group, "linkLabel");
  const action = group.href && actionText
    ? `<a class="collection-group-link" href="${group.href}" target="_blank" rel="noreferrer">${actionText} -></a>`
    : "";

  return `
    <section class="collection-group collection-group-${slug(group.title)}" id="${slug(group.title)}">
      <header class="collection-group-heading">
        <span>${String(index + 1).padStart(2, "0")}</span>
        <h2>${heading}${headingExtra}</h2>
        ${note}
        ${action}
      </header>
      <section class="collection-gallery" aria-label="${group.title}">
        ${group.files.map((item, itemIndex) => renderItem(item, itemIndex, group)).join("")}
      </section>
    </section>
  `;
}

