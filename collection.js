function setupLightbox() {
  let lightbox = document.querySelector(".lightbox");

  if (!lightbox) {
    lightbox = document.createElement("dialog");
    lightbox.className = "lightbox";
    lightbox.setAttribute("aria-label", "Medienansicht");
    lightbox.innerHTML = `
      <button class="close-lightbox" type="button" aria-label="${i18n[getLanguage()].nav.closeMedia}">x</button>
      <img src="" alt="">
    `;
    document.body.append(lightbox);
  }

  const lightboxImage = lightbox.querySelector("img");
  const closeLightbox = lightbox.querySelector(".close-lightbox");
  let lightboxVideo = lightbox.querySelector("video");

  if (!lightbox || !lightboxImage || !closeLightbox) {
    return;
  }

  if (!lightboxVideo) {
    lightboxVideo = document.createElement("video");
    lightboxVideo.className = "lightbox-video";
    lightboxVideo.controls = true;
    lightboxVideo.playsInline = true;
    lightboxVideo.style.display = "none";
    lightbox.append(lightboxVideo);
  }

  let activeIndex = 0;
  const mediaButtons = [
    ...document.querySelectorAll(".collection-image-button")
  ];
  const previousButton = document.createElement("button");
  const nextButton = document.createElement("button");

  previousButton.className = "lightbox-nav lightbox-prev";
  previousButton.type = "button";
  previousButton.setAttribute("aria-label", i18n[getLanguage()].nav.previous);
  previousButton.textContent = "←";

  nextButton.className = "lightbox-nav lightbox-next";
  nextButton.type = "button";
  nextButton.setAttribute("aria-label", i18n[getLanguage()].nav.next);
  nextButton.textContent = "→";

  lightbox.append(previousButton, nextButton);

  function showMedia(index) {
    activeIndex = (index + mediaButtons.length) % mediaButtons.length;
    const button = mediaButtons[activeIndex];
    const isVideo = button.dataset.type === "video";
    const image = button.querySelector("img");

    lightboxImage.classList.add("is-switching");
    lightboxVideo.classList.add("is-switching");
    window.setTimeout(() => {
      if (isVideo) {
        lightboxImage.removeAttribute("src");
        lightboxImage.style.display = "none";
        lightboxVideo.src = button.dataset.full;
        lightboxVideo.style.display = "block";
        lightboxVideo.play();
      } else {
        lightboxVideo.pause();
        lightboxVideo.removeAttribute("src");
        lightboxVideo.style.display = "none";
        lightboxImage.src = button.dataset.full;
        lightboxImage.alt = image.alt;
        lightboxImage.style.display = "block";
      }
      lightboxImage.classList.remove("is-switching");
      lightboxVideo.classList.remove("is-switching");
    }, 120);
  }

  mediaButtons.forEach((button, index) => {
    button.addEventListener("click", () => {
      showMedia(index);
      lightbox.showModal();
    });
  });

  closeLightbox.addEventListener("click", () => {
    lightboxVideo.pause();
    lightbox.close();
  });
  previousButton.addEventListener("click", () => showMedia(activeIndex - 1));
  nextButton.addEventListener("click", () => showMedia(activeIndex + 1));
  lightbox.addEventListener("click", (event) => {
    if (event.target === lightbox) {
      lightboxVideo.pause();
      lightbox.close();
    }
  });
  document.addEventListener("keydown", (event) => {
    if (!lightbox.open) {
      return;
    }

    if (event.key === "ArrowLeft") {
      showMedia(activeIndex - 1);
    }

    if (event.key === "ArrowRight") {
      showMedia(activeIndex + 1);
    }
  });
}

function setupScrollNav() {
  const header = document.querySelector(".site-header");

  if (!header) {
    return;
  }

  let lastScrollY = window.scrollY;
  let ticking = false;

  function updateNavVisibility() {
    const currentScrollY = window.scrollY;
    const isScrollingDown = currentScrollY > lastScrollY;
    const shouldHide = isScrollingDown && currentScrollY > 90;

    header.classList.toggle("nav-hidden", shouldHide);
    lastScrollY = currentScrollY;
    ticking = false;
  }

  window.addEventListener("scroll", () => {
    if (ticking) {
      return;
    }

    ticking = true;
    requestAnimationFrame(updateNavVisibility);
  }, { passive: true });
}

function setupMobileMenu() {
  const header = document.querySelector(".site-header");
  const nav = document.querySelector(".main-nav");
  const toggle = document.querySelector(".mobile-menu-toggle");

  if (!header || !nav || !toggle) {
    return;
  }

  toggle.addEventListener("click", () => {
    const isOpen = nav.classList.toggle("menu-open");
    toggle.classList.toggle("menu-open", isOpen);
    toggle.setAttribute("aria-expanded", String(isOpen));
  });

  nav.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => {
      window.setTimeout(() => {
        nav.classList.remove("menu-open");
        toggle.classList.remove("menu-open");
        toggle.setAttribute("aria-expanded", "false");
      }, 0);
    });
  });
}

