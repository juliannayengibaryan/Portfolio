const root = document.querySelector("#collection-root");
const key = document.body.dataset.collection;
const collection = collections[key];
const activeIndex = Math.max(collectionLinks.findIndex(([, , linkKey]) => linkKey === key), 0);

function fileName(path) {
  return path.split("/").pop().replace(/\.[^.]+$/, "");
}

function slug(value) {
  return value.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "");
}

function renderItem(item, index) {
  if (typeof item === "string") {
    return `
      <article class="collection-item">
        <button class="collection-image-button" type="button" data-full="${item}">
          <img src="${item}" alt="${collection.title} ${index + 1}">
        </button>
      </article>
    `;
  }

  if (item.type === "video") {
    const videoType = item.src.toLowerCase().endsWith(".mov") ? "video/quicktime" : "video/mp4";
    const posterAttr = item.poster ? ` poster="${item.poster}"` : "";
    return `
      <article class="collection-item collection-video">
        <button class="collection-video-button" type="button" data-full="${item.src}" data-type="${videoType}">
          <video muted playsinline preload="metadata"${posterAttr}>
            <source src="${item.src}#t=0.1" type="${videoType}">
          </video>
          <span>${fileName(item.src)}</span>
        </button>
      </article>
    `;
  }

  return `
    <article class="collection-item collection-pdf">
      <a href="${item.src}" target="_blank" rel="noreferrer">
        <strong>${item.title || fileName(item.src)}</strong>
      </a>
    </article>
  `;
}

function renderGallery(items, label) {
  return `
    <section class="collection-gallery" aria-label="${label}">
      ${items.map(renderItem).join("")}
    </section>
  `;
}

function renderGroup(group, index) {
  return `
    <section class="collection-group collection-group-${slug(group.title)}">
      <header class="collection-group-heading">
        <span>${String(index + 1).padStart(2, "0")}</span>
        <h2>${group.title}</h2>
      </header>
      ${renderGallery(group.files, group.title)}
    </section>
  `;
}

function renderCollection() {
  if (!collection) {
    root.innerHTML = "<section class=\"section\"><h1>Ordner nicht gefunden.</h1></section>";
    return;
  }

  document.title = `${collection.title} | Julianna Yengibaryan`;
  root.innerHTML = `
    <section class="collection-hero">
      <span class="collection-number">${String(activeIndex + 1).padStart(2, "0")} —</span>
      <p class="collection-hand">${collection.label.replace("Ordner / ", "")}</p>
      <h1>${collection.title}</h1>
    </section>
    <nav class="collection-switcher" aria-label="Andere Ordner">
      ${collectionLinks.map(([label, href, linkKey]) => `<a class="${linkKey === key ? "active" : ""}" href="${href}">${label}</a>`).join("")}
    </nav>
    ${collection.groups ? `
      <section class="collection-groups" aria-label="${collection.title}">
        ${collection.groups.map(renderGroup).join("")}
      </section>
    ` : renderGallery(collection.files, collection.title)}
  `;
}

function setupLightbox() {
  let lightbox = document.querySelector(".lightbox");

  if (!lightbox) {
    lightbox = document.createElement("dialog");
    lightbox.className = "lightbox";
    lightbox.setAttribute("aria-label", "Medienansicht");
    lightbox.innerHTML = `
      <button class="close-lightbox" type="button" aria-label="Medienansicht schliessen">x</button>
      <img src="" alt="">
    `;
    document.body.append(lightbox);
  }

  const lightboxImage = lightbox.querySelector("img");
  const closeLightbox = lightbox.querySelector(".close-lightbox");
  const lightboxVideo = document.createElement("video");
  lightboxVideo.className = "lightbox-video";
  lightboxVideo.controls = true;
  lightboxVideo.playsInline = true;
  lightbox.append(lightboxVideo);

  if (!lightbox || !lightboxImage || !closeLightbox) {
    return;
  }

  let activeIndex = 0;
  const mediaButtons = [
    ...document.querySelectorAll(".collection-image-button, .collection-video-button")
  ];
  const previousButton = document.createElement("button");
  const nextButton = document.createElement("button");

  previousButton.className = "lightbox-nav lightbox-prev";
  previousButton.type = "button";
  previousButton.setAttribute("aria-label", "Vorheriges Bild");
  previousButton.textContent = "←";

  nextButton.className = "lightbox-nav lightbox-next";
  nextButton.type = "button";
  nextButton.setAttribute("aria-label", "Naechstes Bild");
  nextButton.textContent = "→";

  lightbox.append(previousButton, nextButton);

  function showMedia(index) {
    activeIndex = (index + mediaButtons.length) % mediaButtons.length;
    const button = mediaButtons[activeIndex];
    const image = button.querySelector("img");
    const video = button.querySelector("video");
    const isVideo = button.classList.contains("collection-video-button");

    lightboxImage.classList.add("is-switching");
    lightboxVideo.classList.add("is-switching");
    lightboxVideo.pause();
    window.setTimeout(() => {
      if (isVideo) {
        lightboxImage.hidden = true;
        lightboxVideo.hidden = false;
        lightboxVideo.src = button.dataset.full;
        lightboxVideo.load();
      } else {
        lightboxVideo.hidden = true;
        lightboxImage.hidden = false;
        lightboxVideo.removeAttribute("src");
        lightboxImage.src = button.dataset.full;
        lightboxImage.alt = image.alt;
      }
      lightboxImage.classList.remove("is-switching");
      lightboxVideo.classList.remove("is-switching");
    }, 120);
  }

  mediaButtons.forEach((button, index) => {
    button.addEventListener("click", () => {
      showMedia(index);
      lightbox.showModal();
    });
  });

  closeLightbox.addEventListener("click", () => {
    lightboxVideo.pause();
    lightbox.close();
  });
  previousButton.addEventListener("click", () => showMedia(activeIndex - 1));
  nextButton.addEventListener("click", () => showMedia(activeIndex + 1));
  lightbox.addEventListener("click", (event) => {
    if (event.target === lightbox) {
      lightboxVideo.pause();
      lightbox.close();
    }
  });
  document.addEventListener("keydown", (event) => {
    if (!lightbox.open) {
      return;
    }

    if (event.key === "ArrowLeft") {
      showMedia(activeIndex - 1);
    }

    if (event.key === "ArrowRight") {
      showMedia(activeIndex + 1);
    }
  });
}

function setupScrollNav() {
  const header = document.querySelector(".site-header");

  if (!header) {
    return;
  }

  let lastScrollY = window.scrollY;
  let ticking = false;

  function updateNavVisibility() {
    const currentScrollY = window.scrollY;
    const isScrollingDown = currentScrollY > lastScrollY;
    const shouldHide = isScrollingDown && currentScrollY > 90;

    header.classList.toggle("nav-hidden", shouldHide);
    lastScrollY = currentScrollY;
    ticking = false;
  }

  window.addEventListener("scroll", () => {
    if (ticking) {
      return;
    }

    ticking = true;
    requestAnimationFrame(updateNavVisibility);
  }, { passive: true });
}

renderCollection();
setupLightbox();
setupScrollNav();
